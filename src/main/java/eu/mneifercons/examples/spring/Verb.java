package eu.mneifercons.examples.spring;

public class Verb {
    private String name = null;

    private String first = null;

    private String second = null;

    private String third = null;

    private String firsts = null;

    private String seconds = null;

    private String thirds = null;

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getFirst() {
        return first;
    }

    public void setFirst(String first) {
        this.first = first;
    }

    public String getSecond() {
        return second;
    }

    public void setSecond(String second) {
        this.second = second;
    }

    public String getThird() {
        return third;
    }

    public void setThird(String third) {
        this.third = third;
    }

    public String getFirsts() {
        return firsts;
    }

    public void setFirsts(String firsts) {
        this.firsts = firsts;
    }

    public String getSeconds() {
        return seconds;
    }

    public void setSeconds(String seconds) {
        this.seconds = seconds;
    }

    public String getThirds() {
        return thirds;
    }

    public void setThirds(String thirds) {
        this.thirds = thirds;
    }
}
